t.down()
t.begin_fill()
t.color("green")
t.circle(50)
t.end_fill()

t.up()
t.goto(200,200)
t.down()
t.begin_fill()
t.color("red")
t.circle(50)
t.end_fill()

t.up()
t.goto(-200,200)
t.down()
t.begin_fill()
t.color("yellow")
t.circle(50)
t.end_fill()

t.up()
t.goto(-200,-350)
t.down()
t.begin_fill()
t.color("blue")
t.circle(50)
t.end_fill()

t.up()
t.goto(200,-350)
t.down()
t.begin_fill()
t.color("orange")
t.circle(50)
t.end_fill()

turtle.done()